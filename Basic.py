# Integer
integer = 42

# Floating-point numbers
float_number = 3.14

# Complex numbers
complex_number = 2 + 3j

# Strings
text = 'Hello, world!'

# Lists
list_var = [1, 2, 3, 4, 5]

# Tuples
tuple_var = (10, 20, 30)

# Sets
set_var = {1, 2, 3}

# Dictionaries
dictionary_var = {'key1': 'value1', 'key2': 'value2'}

# Booleans
boolean = True

# None (null type)
null_var = None

# Displaying the content of variables
print(integer)
print(float_number)
print(complex_number)
print(text)
print(list_var)
print(tuple_var)
print(set_var)
print(dictionary_var)
print(boolean)
print(null_var)

# Taking input from the user
num1 = int(input('Enter the first number:'))
